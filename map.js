function addMap(selector, zoom, lat, lon, markers, polygon, scroll1, scroll2) {
    function initialize() {
      var mapOptions = {
        center: new google.maps.LatLng(lat, lon),
        zoom: zoom,
        maxZoom: 16,
        disableDefaultUI: true,
        mapTypeId: google.maps.MapTypeId.ROADMAP
      };
      var bounds = new google.maps.LatLngBounds();
      var poly;
      var map = new google.maps.Map(selector[0], mapOptions);

      var infowindow = new google.maps.InfoWindow();
        google.maps.event.addListener(map, "mousedown", function(e){
            scroll1.disable();
            scroll2.disable();
        });
        
        google.maps.event.addListener(map, "dragstart", function(e){
            scroll1.disable();
            scroll2.disable();
        });

        google.maps.event.addListener(map, "mouseup", function(e){
            scroll1.enable();
            scroll2.enable();
        });
        
        google.maps.event.addListener(map, "dragend", function(e){
            scroll1.enable();
            scroll2.enable();
        });
        
        
      $.each(markers, function(i, m) {
        if (m.lat && m.lon) {
          var loc = new google.maps.LatLng(m.lat, m.lon);
          var marker = new google.maps.Marker({
              position: loc,
              map: map,
              title: m.name,
              animation: google.maps.Animation.DROP // TODO: ran out of time - https://developers.google.com/maps/documentation/javascript/examples/marker-animations-iteration
          });

          var ref = m;
          google.maps.event.addListener(marker, 'click', function(e) {
            infowindow.setContent(getMapMarkerContent(this, ref));
            infowindow.open(map, this);
          });
            

          bounds.extend(loc);
        }
      });
      if (polygon) {
        var coords = [];
        for (var i = 0; i < polygon.length; ++i) {
          var m = polygon[i];
          if (m.lat && m.lon) {
            var loc = new google.maps.LatLng(m.lat, m.lon);
            coords.push(loc)
          }
        }
        poly = new google.maps.Polygon({
          paths: coords,
          strokeColor: '#003399',
          strokeOpacity: 0.25,
          strokeWeight: 2,
          fillColor: '#336699',
          fillOpacity: 0.15
        });
        poly.setMap(map)
      }
      if (markers && markers.length > 1) {
        map.fitBounds(bounds);
        map.panToBounds(bounds); 
      }
    }
    google.maps.event.addDomListener(window, 'load', initialize);
  };

